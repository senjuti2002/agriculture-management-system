 private void loadCostData() {
        try (BufferedReader reader = new BufferedReader(new FileReader(COST_FILE))) {
            String line;
            StringBuilder text = new StringBuilder();
            while ((line = reader.readLine()) != null) {
                text.append(line).append("\n");
            }
            jTextArea1.setText(text.toString());
        } catch (IOException ex) {
            Logger.getLogger(Yield.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadRevenueData() {
        try (BufferedReader reader = new BufferedReader(new FileReader(REVENUE_FILE))) {
            String line;
            StringBuilder text = new StringBuilder();
            while ((line = reader.readLine()) != null) {
                text.append(line).append("\n");
            }
            jTextArea2.setText(text.toString());
        } catch (IOException ex) {
            Logger.getLogger(Yield.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void saveToFile(String data, String fileName) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(fileName))) {
            writer.write(data);
        } catch (IOException ex) {
            Logger.getLogger(Yield.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private double calculateTotalExpense() {
        double seeds = Double.parseDouble(jTextField1.getText());
        double fertilizers = Double.parseDouble(jTextField2.getText());
        double pesticides = Double.parseDouble(jTextField3.getText());
        double irrigation = Double.parseDouble(jTextField4.getText());
        double equipment = Double.parseDouble(jTextField5.getText());
        double labour = Double.parseDouble(jTextField6.getText());
        double utilities = Double.parseDouble(jTextField7.getText());
        double others = Double.parseDouble(jTextField8.getText());

        return seeds + fertilizers + pesticides + irrigation + equipment + labour + utilities + others;
    }

    private double calculateTotalRevenue() {
        double quantityHarvested = Double.parseDouble(jTextField9.getText());
        double sellingPricePerUnit = Double.parseDouble(jTextField10.getText());

        return quantityHarvested * sellingPricePerUnit;
    }
